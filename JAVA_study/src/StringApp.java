
public class StringApp {

	public static void main(String[] args) {
	System.out.println("Hello World"); //String 문자들을 모아두는
	System.out.println('H'); //Character 한개의 문자를 표현
	System.out.println("hello world \n world");
	
	System.out.println("hello world".replace("world", "kdw"));
	
	int a = 1;
	int b = 3;
	System.out.println(a+b);
	
	String c = "KDW";
	System.out.println(c);
	
	

	}

}
